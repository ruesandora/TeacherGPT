package TeacherGPT;

public class R01_isEmpty_isBlank {

    // Define three String variables:
    //str1: Empty, containing only ""
    //str2: Contain only spaces (" ")
    //str3: Let it be a normal String ("Hello")

    // For each String variable, use the .isEmpty() and .isBlank()
    // methods to check if it is empty and print the result.

    // The output should look like this:
    // is str1 empty? true
    // is str2 empty? true
    // str3 empty? false
    public static void main(String[] args) {

        String str1 = "";
        String str2 = " ";
        String str3 = "Hello";

        System.out.println("Is str1 empty? " + str1.isEmpty()); // true
        System.out.println("Is str2 empty? " + str2.isBlank()); // true
        System.out.println("Is str3 empty? " +str3.isEmpty()); // false
    }
}
