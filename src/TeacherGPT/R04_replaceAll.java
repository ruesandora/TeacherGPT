package TeacherGPT;

public class R04_replaceAll {
    public static void main(String[] args) {

        String text = "There are many colours in the rainbow " +
                "like red, green, blue, orange and yellow.";

        text = text.replaceAll("red|green|blue|orange|yellow" ,
                "colour");

        System.out.println(text);
    }
}

class R05_replaceAll_01{
    public static void main(String[] args) {

        String text = "The quick brown fox jumps     over the    lazy    dog.";

        text = text.replaceAll("\\s" , "-");

        System.out.println(text);
    }
}

class R05_replaceAll_02{
    public static void main(String[] args) {

        String str = "Hello World!";
        str = str.replaceAll("o", "x");
        System.out.println(str);

    }
}











