package TeacherGPT;

public class R03_replace {
    public static void main(String[] args) {
        //  Create a String called text with the value
        //  "The quick brown fox jumps over the lazy dog."

        // Use .replace() to:

        //Replace "quick" with "slow"
        //Replace "fox" with "cat"
        //Replace "dog" with "bear"

        String text = "The quick brown fox jumps over the lazy dog";

        text = text.replace("quick" , "slow");
        text = text.replace("fox" , "cat");
        text = text.replace("dog" , "bear");

        System.out.println(text); // The slow brown cat jumps over the lazy bear.
    }
}
