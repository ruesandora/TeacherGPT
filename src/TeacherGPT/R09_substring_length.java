package TeacherGPT;

import java.util.Scanner;

public class R09_substring_length {

    // Write a Java program that asks the user to enter a word
    // and prints the last character of the word on the screen.
    // You can use the substring() and length() methods.
    public static void main(String[] args) {

        Scanner scan = new Scanner(System.in);
        System.out.print("Please enter a word: ");
        String input = scan.next();

        int length = input.length();
        String lastCharacter = input.substring(length - 1);

        System.out.println("Last character: " + lastCharacter);

    }
}
